import mongoose from "mongoose";

// user schema
const userSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
      required: true,
    },
    address: {
      type: String,
      required: true,
    },
    role: {
      type: String,

      default: "user",
    },
  },
  { timestamps: true }
);

// Create User model
const User = mongoose.model("User", userSchema);

export default User;
